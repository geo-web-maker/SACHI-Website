export function getProgrammeBySlug(slug) {
  return programmes.find((p) => p.slug === slug);
}

export function getAdjacentProgrammes(slug) {
  const i = programmes.findIndex((p) => p.slug === slug);
  const prev = programmes[(i - 1 + programmes.length) % programmes.length];
  const next = programmes[(i + 1) % programmes.length];
  return { prev, next };
}
